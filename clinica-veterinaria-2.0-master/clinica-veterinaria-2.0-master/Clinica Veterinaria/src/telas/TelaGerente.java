    private void btnCadastrarAtendenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarAtendenteActionPerformed
        
        double salario;
        if(txtCadastrarSalarioAtendente.getText().trim().isEmpty()){
            txtCadastrarSalarioAtendente.setText("0");
        }
        salario = Double.parseDouble(txtCadastrarSalarioAtendente.getText());
 
        int idade;
        if(txtCadastrarIdadeAtendente.getText().trim().isEmpty()){
            txtCadastrarIdadeAtendente.setText("0");
        }
        idade = Integer.parseInt(txtCadastrarIdadeAtendente.getText());
        
        if(!txtCadastrarCpfAtendente.getText().trim().isEmpty()){
            if(!a.addFuncionario(txtCadastrarNomeAtendente.getText(), txtCadastrarTelefoneAtendente.getText(), txtCadastrarEmailAtendente.getText(), txtCadastrarEnderecoAtendente.getText(), salario , txtCadastrarCpfAtendente.getText(), "Atendente", "null", idade, txtSenhaAtendente.getText())){
                JOptionPane.showMessageDialog(null, "Preencha os campos necessários :C", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Atendente Cadastrado(a) :D", null, WIDTH);
        }else JOptionPane.showMessageDialog(null, "Cpf invalido e(ou) dados não inseridos", null, WIDTH);
        txtCadastrarNomeAtendente.setText(null);
        txtCadastrarTelefoneAtendente.setText(null);
        txtCadastrarEmailAtendente.setText(null);
        txtCadastrarEnderecoAtendente.setText(null);
        txtCadastrarSalarioAtendente.setText(null);
        txtCadastrarCpfAtendente.setText(null);
        txtCadastrarIdadeAtendente.setText(null);
        txtSenhaAtendente.setText(null);
        
    }//GEN-LAST:event_btnCadastrarAtendenteActionPerformed

