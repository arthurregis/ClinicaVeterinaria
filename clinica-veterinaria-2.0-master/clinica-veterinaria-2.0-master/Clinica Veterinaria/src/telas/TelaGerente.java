    private void btnBuscarAtendenteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarAtendenteActionPerformed
        
        if(!txtCadastrarCpfAtendente.getText().trim().isEmpty()){
            
            Atendente atendente = new Atendente();
            atendente = a.buscarAtendente(txtCadastrarCpfAtendente.getText());
            String salario = String.valueOf(atendente.getSalario());
            
            
            if(!atendente.equals(null)){
               TelaPesquisarFuncionario tela = new TelaPesquisarFuncionario();
               tela.preencherDados(atendente.getNome(), atendente.getTelefone(), salario, atendente.getEmail(), atendente.getEndereco(), atendente.getCpf(), atendente.getCargo(), null);
               tela.setVisible(true);
               txtCadastrarCpfAtendente.setText(null);
               
            }
        }else  JOptionPane.showMessageDialog(null, "Preencha o Cpf >:[", null, WIDTH);
        
        
    }//GEN-LAST:event_btnBuscarAtendenteActionPerformed

