    private void btnAlterarInfoClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarInfoClienteActionPerformed
        if(txtEmailCliente.getText().trim().isEmpty() && txtTelefoneCliente.getText().trim().isEmpty()){//alterar o nome
            if(!a.attNome(txtCpfCliente.getText(), txtNomeCliente.getText())){
                JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Dado Alterado com sucesso :D", null, WIDTH);
        }else
        if(txtNomeCliente.getText().trim().isEmpty() && txtEmailCliente.getText().trim().isEmpty()){//alterar o telefone
            if(!a.attTelefone(txtCpfCliente.getText(), txtTelefoneCliente.getText())){
              JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Dado Alterado com sucesso :D", null, WIDTH);
        }else
        if(txtNomeCliente.getText().trim().isEmpty() && txtTelefoneCliente.getText().trim().isEmpty()){//alterar o email
            if(!a.attEmail(txtCpfCliente.getText(), txtEmailCliente.getText())){
                JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Dado Alterado com sucesso :D", null, WIDTH);
        }else
        if(txtEmailCliente.getText().trim().isEmpty()){//alterar nome e telefone
            if(a.attNome(txtCpfCliente.getText(), txtNomeCliente.getText()) && a.attTelefone(txtCpfCliente.getText(), txtTelefoneCliente.getText())){
                JOptionPane.showMessageDialog(null, "Dado Alterado com sucesso :D", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
        }else
        if(txtTelefoneCliente.getText().trim().isEmpty()){//alterar o nome e email
            if(a.attNome(txtCpfCliente.getText(), txtNomeCliente.getText()) && a.attEmail(txtCpfCliente.getText(), txtEmailCliente.getText())){
                JOptionPane.showMessageDialog(null, "Dado Alterado com sucesso :D", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
        }else
        if(txtNomeCliente.getText().trim().isEmpty()){//alterar o email e telefone
            if(a.attEmail(txtCpfCliente.getText(), txtEmailCliente.getText()) && a.attTelefone(txtCpfCliente.getText(), txtTelefoneCliente.getText())){
             JOptionPane.showMessageDialog(null, "Dado Alterado com sucesso :D", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
        }else { //alterar tuso
            if(a.attNome(txtCpfCliente.getText(), txtNomeCliente.getText()) && a.attTelefone(txtCpfCliente.getText(), txtTelefoneCliente.getText()) && a.attEmail(txtCpfCliente.getText(), txtEmailCliente.getText())){
              JOptionPane.showMessageDialog(null, "Dado Alterado com sucesso :D", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
        };
        
        txtNomeCliente.setText(null);
        txtCpfCliente.setText(null);
        txtTelefoneCliente.setText(null);
        txtEmailCliente.setText(null);
        
    }//GEN-LAST:event_btnAlterarInfoClienteActionPerformed

    private void btnPesquisarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarClienteActionPerformed
        
        if(!txtCpfCliente.getText().trim().isEmpty()){
            
            Cliente cliente = new Cliente();
            cliente = a.buscarCliente(txtCpfCliente.getText());
            if(!cliente.equals(null)){
               TelaPesquisarCliente tela = new TelaPesquisarCliente();
               tela.preencherDados(cliente.getNome(), cliente.getCpf(), cliente.getTelefone(), cliente.getEmail());
               tela.setVisible(true);
               txtCpfCliente.setText(null);
               
            }
        }else  JOptionPane.showMessageDialog(null, "Preencha o Cpf >:[", null, WIDTH);
        
    }//GEN-LAST:event_btnPesquisarClienteActionPerformed

