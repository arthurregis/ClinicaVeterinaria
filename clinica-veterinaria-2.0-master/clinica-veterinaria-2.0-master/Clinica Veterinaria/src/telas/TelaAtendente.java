    private void btnCadastrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarClienteActionPerformed
        
        if(!txtCpfCliente.getText().trim().isEmpty()){
            if(!a.cadastrarCliente(txtNomeCliente.getText(), txtCpfCliente.getText(), txtTelefoneCliente.getText(), txtEmailCliente.getText())){
                JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Cliente Cadastrado :)", null, WIDTH);  
        }else JOptionPane.showMessageDialog(null, "Cpf invalido e(ou) dados não inseridos", null, WIDTH);
        
        txtNomeCliente.setText(null);
        txtCpfCliente.setText(null);
        txtTelefoneCliente.setText(null);
        txtEmailCliente.setText(null);
        
    }//GEN-LAST:event_btnCadastrarClienteActionPerformed

    private void btnRemoverClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverClienteActionPerformed
        if(txtCpfCliente.getText().trim().isEmpty()){
            if(!a.removerCliente(1, txtNomeCliente.getText())){
                JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Cliente removido >.<", null, WIDTH);
        }
            
        if(txtNomeCliente.getText().trim().isEmpty()){
            if(!a.removerCliente(2, txtCpfCliente.getText())){
              JOptionPane.showMessageDialog(null, "Algo deu errado :[", null, WIDTH);
            }else JOptionPane.showMessageDialog(null, "Cliente removido >.<", null, WIDTH);
        }
            
        
        txtNomeCliente.setText(null);
        txtCpfCliente.setText(null);
        txtTelefoneCliente.setText(null);
        txtEmailCliente.setText(null);
    }//GEN-LAST:event_btnRemoverClienteActionPerformed

