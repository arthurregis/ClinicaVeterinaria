    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        if(!txtIdExame.getText().trim().isEmpty()){
            
            Exame exame = new Exame();
            exame = a.buscarExame(txtIdExame.getText());
            String valor = String.valueOf(exame.getValor());
            
            if(!a.equals(null)){
                txtNomeCliente.setText(exame.getNomeCliente());
                txtEspecie.setText(exame.getEspecie());
                txtValor.setText(valor);
                txtData.setText(exame.getData());
                txtHorario.setText(exame.getHorario());
            }else JOptionPane.showMessageDialog(null, "Preencha os campos necessÃ¡rios >:[", null, WIDTH);
            
        }else JOptionPane.showMessageDialog(null, "Preencha o Id >:[", null, WIDTH);
        
    }//GEN-LAST:event_btnBuscarActionPerformed

